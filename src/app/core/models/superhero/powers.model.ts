export class Powers {
  intelligence = 0;
  strength = 0;
  speed = 0;
  durability = 0;
  power = 0;
  combat = 0;

  constructor(intelligence?, strength?, speed?, durability?, power?, combat?) {
    this.intelligence = intelligence;
    this.strength = strength;
    this.speed = speed;
    this.durability = durability;
    this.power = power;
    this.combat = combat;
  }
}
